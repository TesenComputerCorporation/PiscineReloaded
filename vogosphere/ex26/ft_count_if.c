int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	r;

	i = 0;
	r = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			r++;
		i++;
	}
	return (r);
}
